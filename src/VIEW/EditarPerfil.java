    private void cargarProductor() {
        //productor/18677972-k
        String data = Unserialize.get();
        Usuario us = Usuario.StringToClass(data);
        WebTarget service = BasicConfig.run();
        String path = "productor/" + us.getRut();
        System.out.println(path);
        String response = service.path(path).request().get(String.class);

        prod = Productor.stringToProducto(response);

        tf_direccion.setText(prod.getDireccion());
        tf_nombre.setText(prod.getNombre());
    }

    private void cargarComunas() {
        WebTarget service = BasicConfig.run();
        String path = "comunas/";
        System.out.println(path);
        String response = service.path(path).request().get(String.class);
        String[] values = response.split("},");
        list = new Comuna[values.length];

        for (int i = 0; i < values.length; i++) {
            list[i] = Comuna.stringToProducto(values[i]);
        }

        jc_comuna.removeAllItems();
        jc_comuna.addItem("Seleccione");

        for (Comuna c : list) {
            jc_comuna.addItem(c.getNombre());
        }

        String nombreComuna = "..";

        for (int i = 0; i < list.length; i++) {
            if (list[i].getId().equals(prod.getComuna())) {
                nombreComuna = list[i].getNombre();
            }
        }

        System.out.println(nombreComuna);

        for (int i = 0; i < jc_comuna.getItemCount(); i++) {
            if (jc_comuna.getItemAt(i).equals(nombreComuna)) {
                jc_comuna.setSelectedIndex(i);
            }
        }
    }

