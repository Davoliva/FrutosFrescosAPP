    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (tf_nombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre ", "InfoBox: Complete los campos", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (tf_direccion.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese la direccion", "InfoBox: Complete los campos", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jc_comuna.getSelectedItem().equals("Seleccione")) {
            JOptionPane.showMessageDialog(null, "Seleccione una comuna", "InfoBox: Complete los campos", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Productor pp = new Productor();
        pp.setNombre(tf_nombre.getText());
        pp.setDireccion(tf_direccion.getText());
        Comuna cc = new Comuna();
        for (int i = 0; i < list.length; i++) {
            if (list[i].getNombre().equals(jc_comuna.getSelectedItem())) {
                cc = list[i];
            }
        }
        try {
            WebTarget service = BasicConfig.run();
            Form form = new Form();
            form.param("nombre", pp.getNombre());
            form.param("comuna", cc.getId());
            form.param("direccion", pp.getDireccion());

            String path = "productor/" + prod.getRut();
            System.out.println(path);
            String response = service.path(path).request().put(Entity.entity(form, MediaType.APPLICATION_FORM_URLENCODED_TYPE), String.class);
            JOptionPane.showMessageDialog(null, "Producto modificado con exito");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No se ha podido actualizar el productor \n Descripción del error "+ex.getMessage(), "InfoBox: Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

